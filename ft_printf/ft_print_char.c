#include "ft_printf.h"

int	ft_putchar_len(char c)
{
	write (1, &c, 1);
	return (1);
}

int	ft_putstr_len(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (write(1, "(null)", 6));
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
